	private static function rus_correct($code) {
		if ($code > 3000 && $code <= 3299) $code = 3000;
		if ($code > 3351 && $code <= 3441) $code = 3351;
		if ($code > 3501 && $code <= 3790) $code = 3501;
		if ($code > 6536 && $code <= 6538) $code = 6536;
		return $code;
	}
	
	public static function get_desc($code) {
		$mcc = '';
		if (isset(self::$mcc_rus[self::rus_correct($code)]))
			$mcc = self::$mcc_rus[self::rus_correct($code)];
		if (isset(self::$mcc_eng[$code]))
			$mcc = $mcc . ($mcc != '' ? "\r\n" : '') . self::$mcc_eng[$code];
		return $mcc;
	}
    
